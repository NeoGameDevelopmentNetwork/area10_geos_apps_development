;*** I/O-Daten zwischenspeichern.
:SaveIOdata		sta	SCPU_HW_EN		;SuperCPU-Optimierung
			lda	SCPU_HW_OPT		;einlesen und zwischenspeichern.
			sta	SCPU_OptFlag
			lda	#%11000000
			sta	SCPU_HW_OPT
			sta	SCPU_HW_DIS

			lda	grcntrl1
			and	#%11101111
			sta	grcntrl1

			ldx	#$00			;ZeroPage zwischenspeichern.
::51			lda	zpage   ,x
