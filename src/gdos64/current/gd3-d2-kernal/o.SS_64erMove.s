			sta	cia1base +13
			sta	cia2base +13

			sta	SCPU_HW_EN		;SuperCPU-Optimierung wieder
			lda	SCPU_OptFlag		;auf Ausgangswerte setzen.
			and	#%11000000
			sta	SCPU_HW_OPT
			sta	SCPU_HW_DIS

			lda	#RAM_64K		;GEOS-RAM aktivieren.
			sta	CPU_DATA
